    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int numero = Integer.parseInt(jSpinner1.getValue().toString());
        int resto = numero % 2;
        float cubo = (float) java.lang.Math.pow(numero, 3);
        float raiz = (float) java.lang.Math.sqrt(numero);
        float raizcub = (float) java.lang.Math.cbrt(numero);
        int absoluto = (int) java.lang.Math.abs(numero);
        
        jLabel9.setText(Integer.toString(resto));
        jLabel10.setText(Float.toString(cubo));
        jLabel11.setText(String.format("%.2f", raiz));
        jLabel12.setText(String.format("%.2f", raizcub));
        jLabel13.setText(Integer.toString(absoluto));
    }//GEN-LAST:event_jButton1ActionPerformed

